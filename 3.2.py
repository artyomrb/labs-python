def sum(a,b,c):
    return a+b+c

def mult(a,b,c):
    return a*b*c

a = int(input('Введите первое число: '))
b = float(input('Введите второе число: '))
c = int(input('Введите третье число: '))
print('Сумма введенных чисел: ', sum(a, b, c))
print('Произведение введенных чисел: ', mult(a, b, c))




    
